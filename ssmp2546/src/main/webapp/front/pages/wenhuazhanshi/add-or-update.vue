	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												biaoti: '',
																wenhualeixing: '',
																fengmian: '',
																yinpin: '',
																suozaidiqu: '',
																xiangxidizhi: '',
																fabushijian: '',
																neirongjieshao: '',
												},
																				wenhualeixingOptions: [],
				wenhualeixingIndex: 0,
																																																												// 登陆用户信息
				user: {},
                                ro:{
                                   biaoti : false,
                                   wenhualeixing : false,
                                   fengmian : false,
                                   yinpin : false,
                                   suozaidiqu : false,
                                   xiangxidizhi : false,
                                   fabushijian : false,
                                   neirongjieshao : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
        		this.ruleForm.fabushijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																						
																		// 下拉框
			res = await this.$api.option(`wenhualeixing`,`wenhualeixing`,{});
			this.wenhualeixingOptions = res.data;
																																													
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`wenhuazhanshi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='biaoti'){
					this.ruleForm.biaoti = obj[o];
					this.ro.biaoti = true;
					continue;
					}
					if(o=='wenhualeixing'){
					this.ruleForm.wenhualeixing = obj[o];
					this.ro.wenhualeixing = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='yinpin'){
					this.ruleForm.yinpin = obj[o];
					this.ro.yinpin = true;
					continue;
					}
					if(o=='suozaidiqu'){
					this.ruleForm.suozaidiqu = obj[o];
					this.ro.suozaidiqu = true;
					continue;
					}
					if(o=='xiangxidizhi'){
					this.ruleForm.xiangxidizhi = obj[o];
					this.ro.xiangxidizhi = true;
					continue;
					}
					if(o=='fabushijian'){
					this.ruleForm.fabushijian = obj[o];
					this.ro.fabushijian = true;
					continue;
					}
					if(o=='neirongjieshao'){
					this.ruleForm.neirongjieshao = obj[o];
					this.ro.neirongjieshao = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																						
			// 多级联动参数
																																																						
																																																			
																																													// 日长控件选择日期时间
			fabushijianConfirm(val) {
				console.log(val)
				this.ruleForm.fabushijian = val.result;
				this.$forceUpdate();
			},
												
															// 下拉变化
			wenhualeixingChange(e) {
				this.wenhualeixingIndex = e.target.value
				this.ruleForm.wenhualeixing = this.wenhualeixingOptions[this.wenhualeixingIndex]
			},
																																										
																											fengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fengmian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																		yinpinTap () {
				let _this = this;
				this.$api.uploadMedia(function(res) {
					_this.ruleForm.yinpin = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
				});
			},
																																										
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																												if((!this.ruleForm.biaoti)){
					this.$utils.msg(`标题不能为空`);
					return
				}
																																												if((!this.ruleForm.wenhualeixing)){
					this.$utils.msg(`文化类型不能为空`);
					return
				}
																																																																																																																																																																																																																																																																if(this.ruleForm.id){
					await this.$api.update(`wenhuazhanshi`, this.ruleForm);
				}else{
					await this.$api.add(`wenhuazhanshi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

